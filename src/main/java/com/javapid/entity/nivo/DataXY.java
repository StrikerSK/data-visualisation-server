package com.javapid.entity.nivo;

public class DataXY {

	private String x;
	private Long y;

	public DataXY() {
	}

	public DataXY(String x, Long y) {
		this.x = x;
		this.y = y;
	}

	public String getX() {
		return x;
	}

	public void setX(String x) {
		this.x = x;
	}

	public Long getY() {
		return y;
	}

	public void setY(Long y) {
		this.y = y;
	}
}
